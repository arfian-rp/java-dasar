public class OperasiPerbandingan {
    public static void main(String[] args) {
        int x = 3;
        int y = 9;

        System.out.println(x >= y);
        System.out.println(x <= y);
        System.out.println(x > y);
        System.out.println(x < y);
        System.out.println(x == y);
        System.out.println(x != y);
    }
}
